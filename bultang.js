const sidebar = document.getElementById('sidebar');
const sidebarToggle = document.getElementById('sidebarToggle');
const cardGrid = document.getElementById('cardGrid');
const searchInput = document.getElementById('searchInput');
const filterSelect = document.getElementById('filterSelect');
const cardModal = document.getElementById('cardModal');
const modalDetails = document.getElementById('modalDetails');
const closeModal = document.querySelector('.close-modal');

// Sidebar Toggle
sidebarToggle.addEventListener('click', () => {
    sidebar.classList.toggle('active');
});

// Scroll Animation Functionality
function applyScrollAnimation() {
    const cards = document.querySelectorAll('.card');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.animationName = 'slideIn';
            }
        });
    }, {
        threshold: 0.1 // Trigger when 10% of card is visible
    });

    cards.forEach(card => {
        observer.observe(card);
    });
}

// Render Cards with Scroll Animation
function renderCards(cards) {
    cardGrid.innerHTML = '';
    cards.forEach((card, index) => {
        const cardElement = document.createElement('div');
        cardElement.classList.add('card');
        cardElement.style.animationDelay = `${0.1 * (index + 1)}s`;
        cardElement.innerHTML = `
            <img src="${card.image}" alt="${card.title}">
            <div class="card-content">
                <h3>${card.title}</h3>
                <p>${card.description}</p>
            </div>
        `;
        cardElement.addEventListener('click', () => openModal(card));
        cardGrid.appendChild(cardElement);
    });
    
    // Apply scroll animation after rendering
    applyScrollAnimation();
}

// Modal Functionality
function openModal(card) {
    modalDetails.innerHTML = `
        <h2>${card.title}</h2>
        <img src="${card.image}" alt="${card.title}" style="width:100%; max-height:400px; object-fit:cover;">
        <p>${card.description}</p>
    `;
    cardModal.style.display = 'block';
}

closeModal.addEventListener('click', () => {
    cardModal.style.display = 'none';
});

window.addEventListener('click', (event) => {
    if (event.target === cardModal) {
        cardModal.style.display = 'none';
    }
});

// Search and Filter
function filterCards() {
    const searchTerm = searchInput.value.toLowerCase();
    const category = filterSelect.value;

    const filteredCards = bultangData.filter(card => {
        const matchesSearch = card.title.toLowerCase().includes(searchTerm) || 
                               card.description.toLowerCase().includes(searchTerm);
        const matchesCategory = category === 'all' || card.category === category;
        
        return matchesSearch && matchesCategory;
    });

    renderCards(filteredCards);
}

searchInput.addEventListener('input', filterCards);
filterSelect.addEventListener('change', filterCards);

// Initial Setup
renderCards(bultangData);